public class Clothing {
    String brand;
    String color;
    String price;

    public Clothing(String brand, String color, String price) {
        this.brand = brand;
        this.color = color;
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public String getColor() {
        return color;
    }

    public String getPrice() {
        return price;
    }
}
